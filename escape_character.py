# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPi") # Pid Apple \r 
print("Red Apple\rPine") # PineApple \r 

# /b : 백스페이스(한 글자 삭제)
print("Redd\bApple") # RedApple

# /t : 탭
print("Red\tApple") # Red     Apple







